    private void EntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EntrarMouseClicked
        // TODO add your handling code here:
         ArrayList<Usuario> usuarios = new ArrayList<Usuario>();

        try ( Scanner scanner = new Scanner(new File("C:\\Users\\real1\\Desktop\\Nueva carpeta (7)\\Restaurante2\\src\\RecursosHumanos\\Usuarios.txt"))) {
            String a = scanner.nextLine();
            String s = scanner.nextLine();
            String c = scanner.nextLine();
            String d = scanner.nextLine();
            String j = scanner.nextLine();
            String k = scanner.nextLine();
            String h = scanner.nextLine();
            String q = scanner.nextLine();
       

            Usuario usu1 = new Usuario(a, s);
            Usuario usu2 = new Usuario(c, d);
            Usuario usu3 = new Usuario(j, k);
            Usuario usu4 = new Usuario(j, k);
        
            usuarios.add(usu1);
            usuarios.add(usu2);
            usuarios.add(usu3);
            usuarios.add(usu4);
          
            
        } catch (FileNotFoundException e) {
            
           System.out.println("Base de datos no encontrado");

        }

        String ID = txtusuario.getText();
        String password = txtpass.getText();

        boolean ingreso = false;

        for (int i = 0; i < usuarios.size(); i++) {

            if (ID.equals(usuarios.get(i).getID())) {
                if (password.equals(usuarios.get(i).getPassword())) {
                    
                    ingreso = true;
                    Administracion abrir = new Administracion();
                    abrir.setVisible(true);
                    this.setVisible(false);
                    break;

                } else {
                 
                    ingreso = true;
                    JOptionPane.showMessageDialog(this,"Error");
                    break;

                }
            }
        }
        if (ingreso == false) {
            
              Errorlogin abrir = new Errorlogin();
            abrir.setVisible(true);
            this.setVisible(false);

        }
    }//GEN-LAST:event_EntrarMouseClicked

